Z_INFINITO = [1.645, 1.960, 2.326, 2.576]

def t_critico(gl, alpha=0.05):
    if gl < 1:
        gl = 1
        
    # Mapeo de alpha a columna
    if alpha > 0.05:
        col = 0 # 10% (90% CI)
    elif alpha > 0.02:
        col = 1 # 5% (95% CI)
    elif alpha > 0.01:
        col = 2 # 2% (98% CI)
    else:
        col = 3 # 1% (99% CI)

    if gl > 30:
        return Z_INFINITO[col]
    if gl in TABLA_T:
        return TABLA_T[gl][col]

    # Si el gl exacto no esta, usar el mas cercano por abajo
    claves = sorted(TABLA_T.keys())
    elegido = claves[0]
    for c in claves:
        if c <= gl:
            elegido = c
    return TABLA_T[elegido][col]
