def f_critico(gl1, gl2, alpha=0.05):
    # Devuelve el valor critico F(gl1, gl2) mas cercano de la tabla.
    # gl1 debe estar entre 1 y 6 (limite de esta tabla reducida)
    if gl1 < 1:
        gl1 = 1
    if gl1 > 6:
        gl1 = 6

    if alpha == 0.01:
        tabla = TABLA_01
    else:
        tabla = TABLA_05

    claves = sorted(tabla.keys())
    elegido = claves[-1]
    for c in claves:
        if gl2 <= c:
            elegido = c
            break
    fila = tabla[elegido]
    return fila[gl1 - 1]
